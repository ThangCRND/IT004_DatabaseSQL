	                              )


--18. Danh sách học viên và điểm thi môn “Co So Du Lieu” (chỉ lấy điểm cao nhất của các lần thi).

SELECT HOCVIEN.*, DIEM AS 'Điểm cao nhất'
FROM HOCVIEN
INNER JOIN KETQUATHI ON HOCVIEN.MAHV = KETQUATHI.MAHV
INNER JOIN MONHOC ON KETQUATHI.MAMH = MONHOC.MAMH
WHERE TENMH = N'Cơ Sở Dữ Liệu' AND DIEM =(
	                                        SELECT MAX(DIEM)
	                                        FROM KETQUATHI
	                                        INNER JOIN MONHOC ON MONHOC.MAMH = KETQUATHI.MAMH 
	                                        WHERE HOCVIEN.MAHV = KETQUATHI.MAHV AND TENMH = N'Cơ Sở Dữ Liệu'
	                                        GROUP BY MAHV
	                                      )
